def put_in_db(msgid, first, last, userus):
    userid = 'id: ' + str(msgid)
    if str(first) != 'None':
        userfname = ' name: ' + str(first)
    else:
        userfname = ''
    if str(last) != 'None':
        userlname = ' ' + str(last)
    else:
        userlname = ''
    useruser = ' username: @' + str(userus)

    users = open("users.txt", "a")
    users.write(userid)
    users.write(userfname)
    users.write(userlname)
    users.write(useruser)
    users.write("\n")
    users.close()

